CREATE TABLE band(
  ID BIGINT PRIMARY KEY NOT NULL AUTO_INCREMENT,
  DATA_BAND DATE NOT NULL,
  NAME_BAND VARCHAR(255) NOT NULL,
  START_TIME TIME NOT NULL,
  END_TIME TIME NOT NULL,
  COUNT_HOURS INT,
  COMMENT VARCHAR(255)
)
  ENGINE InnoDB;


